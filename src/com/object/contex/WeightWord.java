package com.object.contex;

/**
 * 为各个分词分配权重属性
 * @author DaiYuQin
 *
 */
public class WeightWord {
	String word;
	Double weight;
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public Double getWeight() {
		return weight;
	}
	public void setWeight(Double weight) {
		this.weight = weight;
	}
	

}
